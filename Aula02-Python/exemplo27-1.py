variavelString = "Este é um exemplo de variável string"
     
variavelStringAdicional= ", como se vê, com estudo tudo fica fácil!"
     
print ("Conteudo da Variavel String = " +variavelString)       # mostra toda a variavel
     
print("mostra o caracter na posição 3 da string " +variavelString[3])     # mostra o caracter na posição 3 da string
                                 # contagem inicia em zero

print("O tamanho da variavel 'variavelString' = ", len(variavelString))  
print(variavelString[4:17])  # mostra a sub string entre posições 4 e 16                           
                   
stringCompleta = variavelString + variavelStringAdicional
print("O tamanho da variavel 'stringCompleta' = ", len(stringCompleta)) 
     
print("Conteudo da Variavel String Completa = " +stringCompleta) 
